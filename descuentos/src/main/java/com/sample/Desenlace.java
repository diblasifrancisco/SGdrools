package com.sample;

public class Desenlace {

	private int id;
	private String desenlace;

	public Desenlace(int i, String d) {
		id = i;
		desenlace = d;

	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getDesenlace() {
		return desenlace;
	}

	public void setDesenlace(String desenlace) {
		this.desenlace = desenlace;
	}

}
